-- V5: 신규 시스템 운영 및 고급 분석 전용 테이블 생성
-- Oracle 9i 및 PostgreSQL 호환성 고려

-- 1. 공통 코드 테이블
CREATE TABLE IF NOT EXISTS COMMON_CODE (
    GRP_CODE       VARCHAR(10) NOT NULL,
    DTL_CODE       VARCHAR(10) NOT NULL,
    CODE_NAME      VARCHAR(100) NOT NULL,
    IS_ACTIVE      CHAR(1) DEFAULT 'Y',
    SORT_ORDER     NUMERIC(5),
    PRIMARY KEY (GRP_CODE, DTL_CODE)
);

-- 2. 메뉴 마스터 테이블
CREATE TABLE IF NOT EXISTS MENU_MASTER (
    MENU_ID        VARCHAR(10) PRIMARY KEY,
    PARENT_ID      VARCHAR(10),
    MENU_NAME      VARCHAR(100) NOT NULL,
    URL            VARCHAR(255),
    SORT_ORDER     NUMERIC(5),
    IS_USE         CHAR(1) DEFAULT 'Y'
);

-- 3. 유동화(ABCP) 발행 관리 테이블
CREATE TABLE IF NOT EXISTS ABCP_ISSUANCE (
    ISSUE_ID       VARCHAR(30) PRIMARY KEY,
    DEAL_ID        VARCHAR(20) NOT NULL,
    ISSUE_AMOUNT   NUMERIC(19, 4),
    MATURITY_DATE  DATE,
    CE_TYPE        VARCHAR(10), -- CE01(연대보증), CE02(채무인수) 등
    ROLLOVER_COUNT NUMERIC(5) DEFAULT 0
);

